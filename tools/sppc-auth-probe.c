static void dump_bytes(const BYTE *data, UINT size)
{
    UINT i;

    for (i = 0; i < size; ++i)
    {
        if (!(i % 16))
            printf("%04x:", i);
        printf(" %02x", data[i]);
        if ((i % 16) == 15 || i + 1 == size)
            printf("\n");
    }
}

int main(void)
{
    SLGetAuthenticationResultFunc pSLGetAuthenticationResult;
    SLSetAuthenticationDataFunc pSLSetAuthenticationData;
    SLCloseFunc pSLClose;
    SLOpenFunc pSLOpen;
    HMODULE sppc;
    BYTE *result = NULL;
    UINT result_size = 0;
    void *handle = NULL;
    HRESULT hr;

    sppc = LoadLibraryW(L"sppc.dll");
    if (!sppc)
    {
        printf("LoadLibraryW(sppc.dll) failed: %lu\n", GetLastError());
        return 1;
    }

    pSLOpen = (SLOpenFunc)GetProcAddress(sppc, "SLOpen");
    pSLClose = (SLCloseFunc)GetProcAddress(sppc, "SLClose");
    pSLSetAuthenticationData = (SLSetAuthenticationDataFunc)GetProcAddress(sppc, "SLSetAuthenticationData");
    pSLGetAuthenticationResult = (SLGetAuthenticationResultFunc)GetProcAddress(sppc, "SLGetAuthenticationResult");
    if (!pSLOpen || !pSLClose || !pSLSetAuthenticationData || !pSLGetAuthenticationResult)
    {
        printf("missing SPP export\n");
        return 1;
    }

    printf("challenge_size=%u\n", (UINT)sizeof(excel_auth_challenge_20260530));
    hr = pSLOpen(&handle);
    printf("SLOpen hr=0x%08lx handle=%p\n", (unsigned long)hr, handle);
    if (FAILED(hr))
        return 1;

    hr = pSLSetAuthenticationData(handle, sizeof(excel_auth_challenge_20260530),
                                  excel_auth_challenge_20260530);
    printf("SLSetAuthenticationData hr=0x%08lx\n", (unsigned long)hr);
    if (SUCCEEDED(hr))
    {
        hr = pSLGetAuthenticationResult(handle, &result_size, &result);
        printf("SLGetAuthenticationResult hr=0x%08lx size=%u data=%p\n",
               (unsigned long)hr, result_size, result);
        if (result && result_size)
            dump_bytes(result, result_size);
        if (result)
            LocalFree(result);
    }

    pSLClose(handle);
    return 0;
}
